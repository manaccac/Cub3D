#include "../includes/cube.h"

int		ft_check_save(char *str)
{
	if (str[0] == '-' && str[1] == '-' && str[2] == 's'
		&& str[3] == 'a' && str[4] == 'v' && str[5] == 'e' && str[6] == '\0')
		return (1);
	return (0);
}
